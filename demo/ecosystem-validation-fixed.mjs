import { execSync, spawn } from 'node:child_process';
import { existsSync, mkdirSync, writeFileSync, readFileSync, rmSync } from 'node:fs';
import { join, dirname } from 'pathe';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const testDir = join(__dirname, 'test-ecosystem-fixed');
const gitvanBin = join(__dirname, '..', 'bin', 'gitvan.mjs');

// Test results tracking
const results = {
  tests: [],
  passed: 0,
  failed: 0,
  networkEffects: {
    cliCommands: 0,
    filesGenerated: 0,
    jobsExecuted: 0,
    templatesProcessed: 0,
    configsCreated: 0
  }
};

class EcosystemTester {
  constructor() {
    this.testId = 0;
  }

  async test(name, testFn) {
    const id = ++this.testId;
    console.log(`\nüß™ Test ${id}: ${name}`);
    console.log('‚ïê'.repeat(50));

    try {
      const result = await testFn();
      console.log(`‚úÖ PASSED: ${name}`);
      results.passed++;
      results.tests.push({ id, name, status: 'passed', result });
      return result;
    } catch (error) {
      console.error(`‚ùå FAILED: ${name}`);
      console.error(`   Error: ${error.message}`);
      results.failed++;
      results.tests.push({ id, name, status: 'failed', error: error.message });
      throw error;
    }
  }

  exec(command, options = {}) {
    console.log(`   > ${command}`);
    try {
      const output = execSync(command, {
        cwd: testDir,
        encoding: 'utf8',
        stdio: 'pipe',
        timeout: 30000, // 30 second timeout
        ...options
      });
      console.log(`   ${output.trim().split('\n').slice(0, 3).join('\n   ')}`); // Show first 3 lines
      return output;
    } catch (error) {
      console.error(`   ERROR: ${error.message}`);
      throw error;
    }
  }

  gitvan(command) {
    results.networkEffects.cliCommands++;
    return this.exec(`node ${gitvanBin} ${command}`);
  }

  setupTestEnvironment() {
    console.log('üîß Setting up ecosystem test environment...');

    // Clean and create test directory
    if (existsSync(testDir)) {
      rmSync(testDir, { recursive: true, force: true });
    }
    mkdirSync(testDir, { recursive: true });

    // Initialize Git repo
    this.exec('git init');
    this.exec('git config user.name "Ecosystem Tester"');
    this.exec('git config user.email "ecosystem@gitvan.test"');

    // Initialize GitVan
    this.gitvan('init');

    console.log('‚úÖ Ecosystem test environment ready');
  }
}

